n,k=map(int,input().split())

#k ... n の値を取りうる. mとする。
# 和としてあり得るものは、
# m*(2*0+(m-1))/2 ~ m*(2*n+(m-1)*-1)/2 までの値を取りうる
# よって
# m*(2*n+(m-1)*-1)/2 - m*(2*0+(m-1))/2 + 1 通りがありうる。
# 
# mはk~nまでとりうるので
ans=0
for m in range(k,n+1):
    ans+= (m*(2*n+(m-1)*-1)/2 - m*(2*0+(m-1))/2 + 1 )
print(int((ans+1)%(10**9+7)))
